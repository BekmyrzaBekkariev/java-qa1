public class Variables {
	public static void main(String[] args) {
		//Переменные
		System.out.println("Площадь квадрата со стороной " + 5 + "=" + (5 * 5));
		int l = 8;
		System.out.println("Площадь квадрата со стороной " + l + "=" + (l * l));
		int li = 5;
		int lil = li * li;
		System.out.println("Площадь квадрата со стороной " + li + "=" + lil);

// Типы переменных и их значения
		int a = 4; // целое значение

		double b = 3.4; //числа с плавущей точкой
		double c = b * b;
		System.out.println("Площадь " + c);

		String somebody = "Bekmyrza"; // Строка
		System.out.println("Helo " + somebody);
	}
}